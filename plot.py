def plot_lineup(x_home, y_home, x_away, y_away):
    pitch = Pitch(pitch_type="statsbomb",
                orientation="vertical",
                pitch_color='grass',
                line_color='white',
                stripe=True,)
    fig, ax = pitch.draw(figsize=(3, 3))
    ax.scatter(x_home, y_home, c="red")
    ax.scatter(x_away, y_away, c="blue")
    buf = BytesIO()
    fig.savefig(buf, format="png")
    st.image(buf, width=450)
